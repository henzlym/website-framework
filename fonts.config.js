function getGoogleFontLink(requestedFonts) {
	const queryParts = requestedFonts.map((fontName) => {
		const font = fonts[fontName];
		const normal = [];
		const italic = [];

		font.variants.forEach((variant) => {
			const isItalic = variant.includes('italic');
			let weight = variant.replace('italic', '').replace('regular', '400');
			if (isItalic) {
				if (weight == '') {
					weight = '400';
				}
				italic.push(weight);
			} else {
				normal.push(weight);
			}
		});

		let weightsQuery = '';
		if (normal.length > 0) {
			weightsQuery += `0,${normal.join(';0,')}`;
		}
		if (italic.length > 0) {
			if (weightsQuery) {
				weightsQuery += ';';
			}
			weightsQuery += `1,${italic.join(';1,')}`;
		}

		return `${font.name.replace(/ /g, '+')}:ital,wght@${weightsQuery}`;
	});

	return `<link href="https://fonts.googleapis.com/css2?family=${queryParts.join(
		'&family='
	)}&display=swap" rel="stylesheet">`;
}

